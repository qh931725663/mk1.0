package com.haaa.cloudmedical.common.entity;

public class ChronicManageDTO {
	private String chronic_code;
	private String chronic_name;
	private String ahdi;
	private String flag;
	private String statrDate;
	private String ebdDate;
	public String getChronic_code() {
		return chronic_code;
	}
	public void setChronic_code(String chronic_code) {
		this.chronic_code = chronic_code;
	}
	public String getChronic_name() {
		return chronic_name;
	}
	public void setChronic_name(String chronic_name) {
		this.chronic_name = chronic_name;
	}
	public String getAhdi() {
		return ahdi;
	}
	public void setAhdi(String ahdi) {
		this.ahdi = ahdi;
	}
	public String getFlag() {
		return flag;
	}
	public void setFlag(String flag) {
		this.flag = flag;
	}
	public String getStatrDate() {
		return statrDate;
	}
	public void setStatrDate(String statrDate) {
		this.statrDate = statrDate;
	}
	public String getEbdDate() {
		return ebdDate;
	}
	public void setEbdDate(String ebdDate) {
		this.ebdDate = ebdDate;
	}
	@Override
	public String toString() {
		return "ChronicManageDTO [chronic_code=" + chronic_code + ", chronic_name=" + chronic_name + ", ahdi=" + ahdi
				+ ", flag=" + flag + ", statrDate=" + statrDate + ", ebdDate=" + ebdDate + "]";
	}

}
