package com.haaa.cloudmedical.entity;

import java.util.Date;

public class LungCapacity {

	private String order_id;

	private String equipment_use_order_id;

	private String fvc;

	private String fvc_rate;

	private String fev1;

	private String fev1_rate;

	private String pef;

	private String pef_rate;

	private String create_date;

	private String update_date;

	
	
	/**
	 * 
	 */
	public LungCapacity() {
		super();
	}

	/**
	 * @param order_id
	 * @param equipment_use_order_id
	 * @param fvc
	 * @param fvc_rate
	 * @param fev1
	 * @param fev1_rate
	 * @param pef
	 * @param pef_rate
	 * @param create_date
	 * @param update_date
	 */
	public LungCapacity(String order_id, String equipment_use_order_id, String fvc, String fvc_rate, String fev1,
			String fev1_rate, String pef, String pef_rate, String create_date, String update_date) {
		super();
		this.order_id = order_id;
		this.equipment_use_order_id = equipment_use_order_id;
		this.fvc = fvc;
		this.fvc_rate = fvc_rate;
		this.fev1 = fev1;
		this.fev1_rate = fev1_rate;
		this.pef = pef;
		this.pef_rate = pef_rate;
		this.create_date = create_date;
		this.update_date = update_date;
	}

	public String getOrder_id() {
		return order_id;
	}

	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}

	public String getEquipment_use_order_id() {
		return equipment_use_order_id;
	}

	public void setEquipment_use_order_id(String equipment_use_order_id) {
		this.equipment_use_order_id = equipment_use_order_id;
	}

	public String getFvc() {
		return fvc;
	}

	public void setFvc(String fvc) {
		this.fvc = fvc;
	}

	public String getFvc_rate() {
		return fvc_rate;
	}

	public void setFvc_rate(String fvc_rate) {
		this.fvc_rate = fvc_rate;
	}

	public String getFev1() {
		return fev1;
	}

	public void setFev1(String fev1) {
		this.fev1 = fev1;
	}

	public String getFev1_rate() {
		return fev1_rate;
	}

	public void setFev1_rate(String fev1_rate) {
		this.fev1_rate = fev1_rate;
	}

	public String getPef() {
		return pef;
	}

	public void setPef(String pef) {
		this.pef = pef;
	}

	public String getPef_rate() {
		return pef_rate;
	}

	public void setPef_rate(String pef_rate) {
		this.pef_rate = pef_rate;
	}

	

	public String getCreate_date() {
		return create_date;
	}

	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}

	public String getUpdate_date() {
		return update_date;
	}

	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}

	@Override
	public String toString() {
		return "LungCapacity [order_id=" + order_id + ", equipment_use_order_id=" + equipment_use_order_id + ", fvc="
				+ fvc + ", fvc_rate=" + fvc_rate + ", fev1=" + fev1 + ", fev1_rate=" + fev1_rate + ", pef=" + pef
				+ ", pef_rate=" + pef_rate + ", create_date=" + create_date + ", update_date=" + update_date + "]";
	}

	
}
