package com.haaa.cloudmedical.entity;

import java.util.Date;

public class EarThermometer {
	private String order_id;
	
	private String equipment_use_order_id;

	private String temperature;
	
	private String UnitState;
	
	private String create_date;
	
	private String update_date;

	
	
	/**
	 * 
	 */
	public EarThermometer() {
		super();
	}

	/**
	 * @param order_id
	 * @param equipment_use_order_id
	 * @param temperature
	 * @param unitState
	 * @param create_date
	 * @param update_date
	 */
	public EarThermometer(String order_id, String equipment_use_order_id, String temperature, String unitState,
			String create_date, String update_date) {
		super();
		this.order_id = order_id;
		this.equipment_use_order_id = equipment_use_order_id;
		this.temperature = temperature;
		UnitState = unitState;
		this.create_date = create_date;
		this.update_date = update_date;
	}

	public String getOrder_id() {
		return order_id;
	}

	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}

	public String getEquipment_use_order_id() {
		return equipment_use_order_id;
	}

	public void setEquipment_use_order_id(String equipment_use_order_id) {
		this.equipment_use_order_id = equipment_use_order_id;
	}

	public String getTemperature() {
		return temperature;
	}

	public void setTemperature(String temperature) {
		this.temperature = temperature;
	}

	public String getUnitState() {
		return UnitState;
	}

	public void setUnitState(String unitState) {
		UnitState = unitState;
	}

	

	public String getCreate_date() {
		return create_date;
	}

	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}

	public String getUpdate_date() {
		return update_date;
	}

	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}

	@Override
	public String toString() {
		return "EarThermometer [order_id=" + order_id + ", equipment_use_order_id=" + equipment_use_order_id
				+ ", temperature=" + temperature + ", UnitState=" + UnitState + ", create_date=" + create_date
				+ ", update_date=" + update_date + "]";
	}

	
	
}
