package com.haaa.cloudmedical.entity;

import java.util.List;

public class Item {
	private String item_type;
	private String item_name;
	private String plan_level;
	private String level_name;
	private boolean flag;
	private List<Plan> plans;

	public String getItem_type() {
		return item_type;
	}

	public void setItem_type(String item_type) {
		this.item_type = item_type;
	}

	public String getItem_name() {
		return item_name;
	}

	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}

	public String getPlan_level() {
		return plan_level;
	}

	public void setPlan_level(String plan_level) {
		this.plan_level = plan_level;
	}

	public String getLevel_name() {
		return level_name;
	}

	public void setLevel_name(String level_name) {
		this.level_name = level_name;
	}

	public boolean isFlag() {
		return flag;
	}

	public void setFlag(boolean flag) {
		this.flag = flag;
	}

	public List<Plan> getPlans() {
		return plans;
	}

	public void setPlans(List<Plan> plans) {
		this.plans = plans;
	}

	@Override
	public String toString() {
		return "Item [item_type=" + item_type + ", item_name=" + item_name + ", plan_level=" + plan_level
				+ ", level_name=" + level_name + ", flag=" + flag + ", plans=" + plans + "]";
	}

}