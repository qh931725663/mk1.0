package com.haaa.cloudmedical.entity;

import java.util.Date;

/**
 * 
 * @author haaa
 *
 */
public class BloodSugar {
	
	private String order_id;
	
	private String equipment_use_order_id;
	
	private String	measurement_period;
	
	private String BloodSugar;
	
	private String create_date;
	
	private String update_date;
	
	

	/**
	 * 
	 */
	public BloodSugar() {
		super();
	}

	/**
	 * @param order_id
	 * @param equipment_use_order_id
	 * @param measurement_period
	 * @param bloodSugar
	 * @param create_date
	 * @param update_date
	 */
	public BloodSugar(String order_id, String equipment_use_order_id, String measurement_period, String bloodSugar,
			String create_date, String update_date) {
		super();
		this.order_id = order_id;
		this.equipment_use_order_id = equipment_use_order_id;
		this.measurement_period = measurement_period;
		BloodSugar = bloodSugar;
		this.create_date = create_date;
		this.update_date = update_date;
	}

	public String getOrder_id() {
		return order_id;
	}

	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}

	public String getEquipment_use_order_id() {
		return equipment_use_order_id;
	}

	public void setEquipment_use_order_id(String equipment_use_order_id) {
		this.equipment_use_order_id = equipment_use_order_id;
	}

	public String getMeasurement_period() {
		return measurement_period;
	}

	public void setMeasurement_period(String measurement_period) {
		this.measurement_period = measurement_period;
	}

	public String getBloodSugar() {
		return BloodSugar;
	}

	public void setBloodSugar(String bloodSugar) {
		BloodSugar = bloodSugar;
	}

	

	public String getCreate_date() {
		return create_date;
	}

	public void setCreate_date(String create_date) {
		this.create_date = create_date;
	}

	public String getUpdate_date() {
		return update_date;
	}

	public void setUpdate_date(String update_date) {
		this.update_date = update_date;
	}

	@Override
	public String toString() {
		return "BloodSugar [order_id=" + order_id + ", equipment_use_order_id=" + equipment_use_order_id
				+ ", measurement_period=" + measurement_period + ", BloodSugar=" + BloodSugar + ", create_date="
				+ create_date + ", update_date=" + update_date + "]";
	}

	
}
